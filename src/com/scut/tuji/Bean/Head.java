package com.scut.tuji.Bean;

public class Head {
	// Head表的实体类
	private String hid;
	private String hdata;
	private String uno;

	public String getHid() {
		return hid;
	}

	public void setHid(String hid) {
		this.hid = hid;
	}

	public String getHdata() {
		return hdata;
	}

	public void setHdata(String hdata) {
		this.hdata = hdata;
	}

	public String getUno() {
		return uno;
	}

	public void setUno(String uno) {
		this.uno = uno;
	}

}
